        <section class="container">
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    1. Timeline of submission of application for BSTF travel grant
                </p>
                <p class="text-align" id="ans">
                    From the year 2024, every year applicants will have to apply online
                    between January and April. Any application submitted after 11:59 pm
                    Bangladesh Time of 30 April will not be accepted. Within the next
                    month, i.e., in May, applications will be assessed and notification
                    of award will be sent only to the successful candidates by email.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    2. How the applicant will receive notification of award of grant
                    from BSTF
                </p>
                <p class="text-align" id="ans">
                    Successful candidates will receive notification by email. Therefore,
                    all candidates are requested to keep an eye on the inbox as well as
                    the spam folder of the email they provided in their application. If
                    an applicant does not receive notification by the first week of
                    June, it will imply that her/his application has not been approved
                    and in that case, applicants are requested not to contact BSTF by
                    email or phone to ask status of their application. However, if an
                    applicant is notified of award of travel grant and yet the applicant
                    does not receive the scholarship in the designated bank account by
                    30 June, BSTF may be contacted by email or phone.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    3. Eligibility for application
                </p>
                <p class="text-align" id="ans">
                    Bangladeshi students who went abroad from Bangladesh for higher
                    studies from the first day of April of the year preceding the
                    application year until the end of March of the application year will
                    be eligible to apply. Example: Applicants who went abroad between 01
                    April 2022 and 31 March 2023 will be eligible to apply for grant.
                    Therefore, in this case, students who went abroad before 01 April
                    2022 or after 31 March 2023 will not be eligible to apply. Moreover,
                    students who have already been awarded BSTF grant previously or have
                    been provided air ticket for travel from elsewhere will not be
                    eligible to apply. Legal steps will be taken against an applicant if
                    she/he conceals or fabricates information to obtain grant
                    fraudulently.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">4. Designated bank account</p>
                <p class="text-align" id="ans">
                    Travel grant will be remitted only to any one of the applicant’s
                    own, father’s, mother’s, spouse’s, brother’s, sister’s bank
                    accounts. The applicant will clearly mention in the application how
                    the money should be transferred and steps will be take accordingly.
                    Any other bank account other than those mentioned in the preceding
                    sentence shall not be eligible unless there is compelling reason.
                    The designated bank account must be maintained with a scheduled
                    commercial bank located in Bangladesh. If the bank account
                    information is wrong and as a result travel grant cannot be sent to
                    the designated bank account, the application will be considered
                    rejected.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    5. Whether hard copies of application documents are required to
                    submit
                </p>
                <p class="text-align" id="ans">
                    No hard copies are required. Applicants will only need to upload
                    soft copies of required documents through online application system.
                    However, the applicants may still need to send hard copies of
                    documents as and when BSTF requests for.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    6. Documents that the applicants will need to submit online
                </p>
                <p class="text-align" id="ans">
                    Applicants will need to submit online copies of passport
                    (information and pages with immigration seals), NID, educational
                    certificates, annual income statement of self and/or parents,
                    passport size photo, enrollment certificate (mentioning date of
                    enrollment, course name, date of commencement of course etc.) issued
                    by the foreign university in which applicant has been admitted,
                    plane ticket, all boarding passes etc.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    7. How to submit application online?
                </p>
                <p class="text-align" id="ans">
                    At the beginning of the online application, all applicants will need
                    to create an account using their emails and later all communications
                    to and from the BSTF will be done through this email. Therefore, the
                    applicants are requested to choose email address carefully.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    8. What should I do if I do not have the required documents at the
                    time of online application?
                </p>
                <p class="text-align" id="ans">
                    Applicant will be able to save all the steps of the application at
                    any time and then later the applicant will be able to add or replace
                    any information or document to the saved application. Therefore, if
                    any required document is not at hand at the time of application, the
                    applicant will be able to return to the saved application and
                    continue. However, applicants should note that once the application
                    is finally submitted after completing all steps and review, the
                    applicant will not be able to edit the application anymore and hence
                    the application may be rejected. Therefore, the applicants should
                    take extreme care at the time of submission of application.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    9. May I submit application using birth registration instead of NID?
                </p>
                <p class="text-align" id="ans">
                    Yes, if the birth registration can be verified online. However, in
                    this case, the birth registration number must be exactly the same as
                    the personal number mentioned in the applicant’s passport.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    10. Can I apply for travel grant in case of travel by bus or train?
                </p>
                <p class="text-align" id="ans">
                    Travel by bus or train only to India will be eligible for
                    application. Adequate supporting documents in this regard will need
                    to be submitted.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    11. Can there be more than one applicant from the same family?
                </p>
                <p class="text-align" id="ans">
                    Yes, all the eligible applicants from the same family will be
                    allowed to submit application separately.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    12. If there is only departure seal but no arrival seal in the
                    passport, will the application be allowed?
                </p>
                <p class="text-align" id="ans">
                    If there is no arrival seal in the passport, reason for such case
                    will be duly verified and if found that the foreign immigration does
                    not put arrival seal in the passport, the application will be
                    allowed. However, other related document in support of arrival will
                    be required.
                </p>
            </article>
            <article class="my-5">
                <p class="faq-heading fw-bold faq-text" id="ques">
                    13. Will all the successful applicants receive the same amount of
                    travel grant?
                </p>
                <p class="text-align" id="ans">
                    Travel grant is generally determined based on the airfare.
                    Therefore, amount of travel grant will differ from applicant to
                    applicant based on their location abroad.
                </p>
            </article>
